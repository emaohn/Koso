import Foundation
import UIKit.UIColor

extension UIColor {
    static var tcDarkGrey: UIColor {
        return UIColor(red: 41/255, green: 49/255, blue: 50/255, alpha: 1)
    }
    
    static var tcBlueGrey: UIColor {
        return UIColor(red: 93/255, green: 115/255, blue: 126/255, alpha: 1)
    }
    
    static var tcOffWhite: UIColor {
        return UIColor(red: 255/255, green: 251/255, blue: 252/255, alpha: 1)
    }
    
    static var tcTurquoise: UIColor {
        return UIColor(red: 98/255, green: 187/255, blue: 193/255, alpha: 1)
    }
    
    static var tcBlue: UIColor {
        return UIColor(red: 108/255, green: 145/255, blue: 191/255, alpha: 1)
    }
}
